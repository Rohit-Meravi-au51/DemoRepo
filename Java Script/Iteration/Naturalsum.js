// program to display the sum of natural numbers
let sum = 0;
n = 10

// looping from i = 1 to n
// in each iteration, i is increased by 1
for (let i = 1; i <= n; i++) 
{
    console.log(i);
console.log(sum);
    sum += i;  // sum = sum + i
   
}
console.log('Sum of first 10 natural number is:', sum);